import Foundation

func dateFormatter(_ dateString: String) -> Date {
    let formatter = DateFormatter()
    formatter.dateFormat = "yyyy/MM/dd HH:mm"
    guard let date = formatter.date(from: dateString) else { return Date() }
    return date
}

